create materialized view test_rollup1 as (
    select name,count(*)
    from test
    group by name
);

select pgrollup_from_matview('test_rollup1', partition_clause=>'partition by range ("test.name")', dry_run=>false);

CREATE TABLE test_rollup1_p1 PARTITION OF test_rollup1_raw
    FOR VALUES FROM ('a') TO ('c');
CREATE TABLE test_rollup1_p2 PARTITION OF test_rollup1_raw
    FOR VALUES FROM ('c') TO ('d');
CREATE TABLE test_rollup1_p3 PARTITION OF test_rollup1_raw DEFAULT;

-- print the names of the partitions
SELECT
    nmsp_parent.nspname AS parent_schema,
    parent.relname      AS parent,
    nmsp_child.nspname  AS child_schema,
    child.relname       AS child
FROM pg_inherits
    JOIN pg_class parent            ON pg_inherits.inhparent = parent.oid
    JOIN pg_class child             ON pg_inherits.inhrelid   = child.oid
    JOIN pg_namespace nmsp_parent   ON nmsp_parent.oid  = parent.relnamespace
    JOIN pg_namespace nmsp_child    ON nmsp_child.oid   = child.relnamespace
WHERE parent.relname='test_rollup1_raw';

select assert_rollup('test_rollup1');
